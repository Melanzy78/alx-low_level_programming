#include <stdio.h>
/**
 * main - print if the number is postive, zero, or negative
 *
 * Description: using the main function
 * this program prints "Programming is positive, zero, or negative"
 * Return: 0
 */
int main(void)
{
	char ch;
	char CH;

	for (ch = 'a' ; ch <= 'z' ; ch++)
		putchar(ch);

	for (CH = 'A' ; CH <= 'Z' ; CH++)
		putchar(CH);
	putchar('\n');
	return (0);
}
